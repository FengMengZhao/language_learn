public class BitWise{
    public static void main(String args[]){
        System.out.println(3 >> 1) ;
        System.out.println(3 << 1) ;
        System.out.println(501 >>> 1);
        System.out.println(501 >> 1);
        System.out.println(1024 >>> 2);
        System.out.println(1024 >>> 3);
        System.out.println(1024 >>> 4);
    }
}
